namespace LoggingLibrary
{
  /// <summary>
  /// Enumerates all log levels
  /// </summary>
  public enum LogLevel
  {
    /// <summary>
    /// Unspecified log level
    /// </summary>
    Unspecified = 0,

    /// <summary>
    /// Debug log entry
    /// </summary>
    Debug,

    /// <summary>
    /// Info log entry
    /// </summary>
    Info,

    /// <summary>
    /// Warning log entry
    /// </summary>
    Warning,

    /// <summary>
    /// Error log entry
    /// </summary>
    Error,

    /// <summary>
    /// Critical error log entry
    /// </summary>
    Critical
  }
}
